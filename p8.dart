// Control Flow Statments.
void main() {
  // if
  var a = 10;
  if (a == 10) {
    print("It's");
  }

  // if else
  var b = 20;
  if (b == 10) {
    print(b);
  } else {
    print("B is not equal to 10");
  }

  // if else else if
  var c = 20;
  if (c < 20) {
    print("C is Less Than 20");
  } 
  else if (c == 20) {
    print(c);
  } 
  else {
    print("This Value is Greater than 20");
  }
}
